import java.util.Scanner;

public class FiveToTwenty
{
    /* Author: Thomas Hazekamp
    * Date : 27/01/22
    * Description: Using a loop it will print out the numbers 5 to 20 inclusive
    */

    public static void main(String []args)
    {
        int i = 5;
        while(i <= 20)
        {
            System.out.print(i + " ");
            i++;
        }

        System.out.println(); 
    }
}