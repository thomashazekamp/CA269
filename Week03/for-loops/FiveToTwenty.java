import java.util.Scanner;

public class FiveToTwenty
{
    /* Author: Thomas Hazekamp
    * Date : 27/01/22
    * Description: Using a loop it will print out the numbers 5 to 20
    */

    public static void main(String []args)
    {
        for (int i = 5; i <= 20; i++)
        {
            System.out.print(i + " ");
        }

        System.out.println();
    }
}