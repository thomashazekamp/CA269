import java.util.Scanner;

public class OneToTen
{
    /* Author: Thomas Hazekamp
    * Date : 27/01/22
    * Description: Using a loop it will print out the numbers 1 to 10
    */

    public static void main(String []args)
    {
        int i = 1;
        while(i <= 10)
        {
            System.out.print(i + " ");
            i++;
        }

        System.out.println(); 
    }
}