import java.util.Scanner;

public class OneToTen
{
    /* Author: Thomas Hazekamp
    * Date : 27/01/22
    * Description: Using a loop it will print out the numbers 1 to 10
    */

    public static void main(String []args)
    {
        for (int i = 1; i <= 10; i++)
        {
            System.out.print(i + " ");
        }

        System.out.println();
    }
}